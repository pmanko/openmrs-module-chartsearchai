#!/usr/bin/env python3
"""(Re)build the honest-metric gold from the adjudicated focus labels.

gold[patientUuid|topic] = {present: bool, ontopic: {uuid: text}, focus_uuids: {uuid: text}}
  - ontopic: on-topic records, from focus_labels.json on-topic RANKS mapped through the focus dumps
    (the recall anchor + in-focus precision labels).
  - focus_uuids: ALL top-30 focus records (so metric_score can tell a known in-focus off-topic
    citation from a true out-of-focus unknown). REQUIRED by metric_score.py.

Inputs: focus_labels.json (alongside this script) + the focus dumps. The dumps are NOT committed
(they are regenerated by flag-gated instrumentation in QueryStoreChartBuilder); point FOCUS_DUMPS
at them. Output overwrites metric_gold.json alongside this script — but only if dumps are found,
so a dump-less run can't clobber the committed gold."""
import json, glob, os, sys

HERE = os.path.dirname(os.path.abspath(__file__))
DUMPS = os.environ.get('FOCUS_DUMPS', '/tmp/focusdumps2')
Q2T = {'Is the patient enrolled in any programs?': 'programs', 'Does the patient have any allergies?': 'allergies',
       'What medications is the patient taking?': 'medications', 'Does the patient have any eye problems?': 'eye',
       'Does the patient have any heart or cardiac problems?': 'heart', 'Has the patient had any fractures or broken bones?': 'fractures',
       'Does the patient have any kidney problems?': 'kidney', 'Does the patient have any mental health or psychiatric conditions?': 'mental'}

labels = json.load(open(os.path.join(HERE, 'focus_labels.json')))
dump_files = glob.glob(DUMPS + '/*.json')
if not dump_files:
    sys.exit('ERROR: no focus dumps in %s (set FOCUS_DUMPS). Refusing to overwrite metric_gold.json with empty gold.' % DUMPS)
dumps = {}
for p in dump_files:
    d = json.load(open(p))
    dumps[(d['patient'], Q2T[d['question']])] = {h['rank']: (h['uuid'], h.get('text', '')) for h in d['hits']}

gold = {}
for cellkey, lab in labels.items():
    if cellkey.startswith('_'):
        continue
    uuid, topic = cellkey.split('|')
    rank2 = dumps.get((uuid, topic), {})
    if not rank2:
        print('WARN: no focus dump for %s — gold cell will be empty' % cellkey)
    ontopic = {rank2[r][0]: rank2[r][1] for r in lab.get('ranks', []) if r in rank2}
    focus = {v[0]: v[1] for v in rank2.values()}
    gold[cellkey] = {'present': lab['present'], 'ontopic': ontopic, 'focus_uuids': focus}

json.dump(gold, open(os.path.join(HERE, 'metric_gold.json'), 'w'), indent=1)
present = [k for k, v in gold.items() if v['present']]
print('gold cells: %d  present: %d  absent: %d' % (len(gold), len(present), len(gold) - len(present)))
for k, v in sorted(gold.items()):
    if v['present']:
        print('  %-55s ontopic=%d focus=%d' % (k.split('|')[1] + ' (' + k[:8] + ')', len(v['ontopic']), len(v['focus_uuids'])))
